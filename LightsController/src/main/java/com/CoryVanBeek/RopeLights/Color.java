package com.CoryVanBeek.RopeLights;

/**
 * A class used to hold an RGB color value
 *
 * @author Cory Van Beek
 */
public class Color {
    public Color() {}

    public Color(int r, int g, int b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public int r;
    public int g;
    public int b;
}
